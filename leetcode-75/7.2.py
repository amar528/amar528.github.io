import math
from time import perf_counter


class Solution:
    def productExceptSelf(self, nums):
        n = len(nums)
        pre = 1
        post = 1
        result = [1] * n

        for i in range(n):
            result[i] *= pre
            pre *= nums[i]
            post_idx = -1 - i
            result[post_idx] *= post
            post *= nums[post_idx]
        return result


